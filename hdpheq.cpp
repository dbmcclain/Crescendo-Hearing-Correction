void TCrescendo::set_headphone(t_EQStruct *eqtbl, bool force)
{
	static Float64 hdphEQ[129];
    
    if((eqtbl != m_HdphEQ_basis) || force)
    {
        interpolate_eqStruct(eqtbl, hdphEQ, &ampl10);
        m_HdphEQ_basis = eqtbl;
        m_HdphEQ = hdphEQ;
        invalidate_unified_filter();
    }
}

